#include "push-swap.h"

void	ft_pa(t_stack_a **a, t_stack_b **b)
{
	if (*b)
		*a = *b;
	write(1, "pa\n", 3);
}

void	ft_pb(t_stack_a **a, t_stack_b **b)
{
    if (*a)
        *b = *a;
	write(1, "pb\n", 3);
}

